namespace fCraft.Drawing {
    /// <summary> Axis used for drawing. X/Y are horizontal axes, and Z is vertical. </summary>
    public enum Axis {
        /// <summary> First horizontal axis (Notch's X). Corresponds to "Width". </summary>
        X,

        /// <summary> Second horizontal axis (Notch's Z). Corresponds to "Length". </summary>
        Y,

        /// <summary> Vertical axis (Notch's Y). Corresponds to "Height". </summary>
        Z
    }
}